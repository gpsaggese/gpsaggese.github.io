def load_dialogue_model(model_name: str = "microsoft/DialoGPT-small") -> Tuple[AutoModelForCausalLM, AutoTokenizer]:
    """
    Load a pre-trained conversational model (DialoGPT) and its tokenizer.
    The model is wrapped with a value head for PPO training.

    :param model_name: name or path of the pretrained DialoGPT model
    :return: a tuple (model, tokenizer) where model is a AutoModelForCausalLMWithValueHead
    """
    # Load tokenizer and model
    tokenizer = AutoTokenizer.from_pretrained(model_name)
    model = AutoModelForCausalLMWithValueHead.from_pretrained(model_name)
    # Ensure tokenizer has a padding token to avoid warnings (DialoGPT uses EOS as pad)
    tokenizer.pad_token = tokenizer.eos_token
    _LOG.info("Loaded model `%s` with %s parameters", model_name, model.num_parameters()) if hasattr(model, "num_parameters") else None
    return model, tokenizer

def generate_response(model: AutoModelForCausalLM, tokenizer: AutoTokenizer, context: List[str], max_new_tokens: int = 50) -> str:
    """
    Generate a single response from the model given a dialogue context.

    :param model: the language model (with value head if PPO) to use for generation
    :param tokenizer: tokenizer for the model
    :param context: list of utterances representing the dialogue history (context)
    :param max_new_tokens: maximum number of tokens to generate for the response
    :return: the generated response text (str)
    """
    # Prepare input IDs from context
    input_text = format_context(context, tokenizer)
    input_ids = tokenizer.encode(input_text, return_tensors="pt")
    input_ids = input_ids.to(model.device)  # move to model device (GPU/CPU)
    # Generate a response
    output_ids = model.generate(
        input_ids,
        max_new_tokens=max_new_tokens,
        pad_token_id=tokenizer.eos_token_id,
        do_sample=True,  # use sampling for more natural responses
        top_p=0.9,       # nucleus sampling for diversity
        temperature=1.0
    )
    # The generated output includes the context and response; extract the response part
    generated_ids = output_ids[0][input_ids.shape[-1]:]  # exclude the context tokens
    response_text = tokenizer.decode(generated_ids, skip_special_tokens=True)
    return response_text

