def train_dialogue_model(
    model: AutoModelForCausalLM,
    tokenizer: AutoTokenizer,
    contexts: List[List[str]],
    num_steps: int = 100,
    batch_size: int = 8,
    ppo_kwargs: Optional[Dict] = None
) -> AutoModelForCausalLM:
    """
    Fine-tune the dialogue model using Proximal Policy Optimization (PPO) on the given contexts.

    At each step, a batch of contexts is sampled, the model generates responses, and a reward is computed for each
    using `compute_reward`. The PPO trainer then updates the model to maximize the expected reward.

    :param model: the model to fine-tune (should already be loaded with a value head)
    :param tokenizer: tokenizer for the model
    :param contexts: list of dialogue contexts (each a list of utterances) to sample from for training
    :param num_steps: total number of PPO update steps to perform
    :param batch_size: number of context-response samples per PPO update
    :param ppo_kwargs: optional dict of additional PPOTrainer configuration (learning rate, etc.)
    :return: the fine-tuned model (updated in-place)
    """
    # Prepare reference model for KL penalty and configure PPO
    ref_model = create_reference_model(model)
    ppo_config = PPOConfig(batch_size=batch_size, **(ppo_kwargs or {}))
    trainer = PPOTrainer(ppo_config, model, ref_model, tokenizer, **(ppo_kwargs or {}))
    device = model.device

    # Training loop
    for step in range(1, num_steps + 1):
        # Sample a batch of contexts from the provided list
        batch_contexts = random.sample(contexts, batch_size) if batch_size <= len(contexts) else contexts
        query_tensors = []
        response_tensors = []
        rewards = []
        # Generate responses and compute rewards for each context in the batch
        for ctx in batch_contexts:
            # Encode context and generate response using current model
            context_text = format_context(ctx, tokenizer)
            query_tensor = tokenizer.encode(context_text, return_tensors="pt").to(device)
            # Generate a response (one sample per context)
            generated_tensor = model.generate(
                query_tensor,
                max_new_tokens=50,
                pad_token_id=tokenizer.eos_token_id,
                do_sample=True, top_p=0.9, temperature=1.0
            )
            # Extract the response portion (skip input context)
            response_tensor = generated_tensor[:, query_tensor.shape[-1]:]
            response_text = tokenizer.decode(response_tensor.squeeze(), skip_special_tokens=True)
            # Compute reward for the generated response
            reward_value = compute_reward(response_text, use_length_heuristic=True)
            query_tensors.append(query_tensor[0])
            response_tensors.append(response_tensor[0])
            rewards.append(torch.tensor(reward_value))
        # Perform one PPO update step with the collected batch
        trainer.step(query_tensors, response_tensors, rewards)
        if step % 10 == 0 or step == num_steps:
            _LOG.info("PPO training step %d/%d complete", step, num_steps)
    return model