def calculate_distinct_n(responses: List[str], n: int = 1) -> float:
    """
    Calculate the Distinct-N metric for a list of generated responses:contentReference[oaicite:1]{index=1}.
    Distinct-N is the number of unique n-grams in the responses divided by the total number of n-grams.

    :param responses: list of response strings generated by the model
    :param n: size of n-gram (e.g., 1 for distinct-1, 2 for distinct-2)
    :return: distinct-n score (fraction of unique n-grams)
    """
    if not responses:
        return 0.0
    total_ngrams = 0
    unique_ngrams = set()
    for resp in responses:
        # Tokenize by whitespace for n-grams (simple approach)
        words = resp.split()
        # Extract n-grams from the response
        for i in range(len(words) - n + 1):
            ngram = tuple(words[i:i+n])
            unique_ngrams.add(ngram)
            total_ngrams += 1
    if total_ngrams == 0:
        return 0.0
    return len(unique_ngrams) / total_ngrams

def evaluate_sentiment(responses: List[str]) -> Dict[str, float]:
    """
    Evaluate the sentiment of a list of responses, returning the proportion of positive responses and average sentiment score.

    The average sentiment score is computed by assigning +1 to a perfectly positive response and -1 to a perfectly negative response.

    :param responses: list of generated response strings
    :return: dictionary with 'positive_fraction' and 'average_score'
    """
    if not responses:
        return {"positive_fraction": 0.0, "average_score": 0.0}
    global _sentiment_analyzer
    if _sentiment_analyzer is None:
        _sentiment_analyzer = pipeline("sentiment-analysis", model="distilbert-base-uncased-finetuned-sst-2-english", device=0 if torch.cuda.is_available() else -1)
    results = _sentiment_analyzer(responses)
    positive_count = 0
    total_score = 0.0
    for res in results:
        label = res['label']
        score = res['score']
        if label == "POSITIVE":
            positive_count += 1
            total_score += score  # positive score
        else:
            total_score -= score  # negative score counts negatively
    positive_fraction = positive_count / len(responses)
    average_score = total_score / len(responses)
    return {"positive_fraction": positive_fraction, "average_score": average_score}

