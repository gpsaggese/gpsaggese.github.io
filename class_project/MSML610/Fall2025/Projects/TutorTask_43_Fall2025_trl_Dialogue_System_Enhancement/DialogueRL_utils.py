# Initialize a sentiment analysis pipeline (global singleton) when first needed
_sentiment_analyzer = None

def compute_reward(response_text: str, use_length_heuristic: bool = True) -> float:
    """
    Compute a scalar reward for a generated response based on its sentiment (and optionally length).
    Positive responses receive higher reward, negative responses receive lower (even negative) reward.

    The sentiment is evaluated via a pretrained sentiment classifier. An optional length heuristic can
    give a small penalty to very short responses to encourage more informative outputs.

    :param response_text: the generated response (single utterance) to evaluate
    :param use_length_heuristic: whether to adjust reward based on response length
    :return: reward value (float), typically in the range [-1.0, 1.0]
    """
    global _sentiment_analyzer
    if _sentiment_analyzer is None:
        # Load a sentiment analysis pipeline (e.g., distilbert-base-uncased-finetuned-sst-2-english)
        _sentiment_analyzer = pipeline("sentiment-analysis", model="distilbert-base-uncased-finetuned-sst-2-english", device=0 if torch.cuda.is_available() else -1)
    # Get sentiment prediction
    result = _sentiment_analyzer(response_text[:512])  # limit length for sentiment model
    label = result[0]['label']
    score = result[0]['score']
    # Base sentiment reward: +score for positive, -score for negative
    sentiment_reward = score if label == "POSITIVE" else -score
    reward = sentiment_reward
    # Optional length-based heuristic: penalize very short responses, mildly reward adequate length
    if use_length_heuristic:
        num_tokens = len(response_text.split())
        if num_tokens < 5:
            reward -= 0.2  # discourage trivially short responses
        elif num_tokens > 50:
            reward -= 0.1  # very long rambling responses might be penalized
        else:
            # small bonus for having at least a minimal length
            reward += 0.1
    return reward

