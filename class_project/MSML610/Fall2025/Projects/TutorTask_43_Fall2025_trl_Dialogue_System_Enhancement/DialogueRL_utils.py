def prepare_context_response_pairs(dialogs: List[Dict], context_length: int = 3) -> List[Tuple[List[str], str]]:
    """
    Convert a list of dialogues into context-response pairs for training or evaluation.
    Each dialogue entry in `dialogs` should have a 'dialog' key with a list of utterances:contentReference[oaicite:0]{index=0}.

    This function creates multiple pairs from each dialogue by taking the last 1 to `context_length`
    utterances as the context and the next utterance as the response.

    :param dialogs: list of dataset entries, each containing a 'dialog' (list of utterance strings)
    :param context_length: how many past utterances to include in context (max)
    :return: list of (context, response) pairs, where context is a list of utterances and response is a string
    """
    pairs: List[Tuple[List[str], str]] = []
    for entry in dialogs:
        conv = entry.get("dialog", entry)  # handle either dict with 'dialog' or raw list
        if not isinstance(conv, list):
            continue  # skip if dialog is missing or not a list
        n = len(conv)
        # Iterate through conversation turns, producing context-response pairs
        for i in range(1, n):
            # Determine context window for this response
            start_idx = max(0, i - context_length)
            context_utts = conv[start_idx:i]  # last up to context_length utterances before i
            response = conv[i]
            pairs.append((context_utts, response))
    return pairs

def format_context(utterances: List[str], tokenizer: AutoTokenizer) -> str:
    """
    Format a list of utterances into a single string for the model, separating turns with the EOS token.
    This helps DialoGPT identify conversation boundaries.

    :param utterances: list of utterance strings in the dialogue context
    :param tokenizer: tokenizer (with an EOS token defined) for the model
    :return: a single string with EOS tokens appended after each utterance
    """
    eos_token = tokenizer.eos_token or "<|endoftext|>"
    if not utterances:
        return eos_token  # return just EOS if no context
    # Join utterances with EOS in between, and append EOS at end to indicate end-of-context
    return eos_token.join(utterances) + eos_token

